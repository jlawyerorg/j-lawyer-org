package com.jdimension.jlawyer.security.jwt;

/**
 * Signals that a JSON Web Token could not be verified — malformed structure,
 * unexpected algorithm, invalid signature, expiry, or a failed issuer/audience
 * check. Callers should treat any {@code JwtException} as "not authenticated".
 *
 * @author jens
 */
public class JwtException extends Exception {

    public JwtException(String message) {
        super(message);
    }

    public JwtException(String message, Throwable cause) {
        super(message, cause);
    }
}
