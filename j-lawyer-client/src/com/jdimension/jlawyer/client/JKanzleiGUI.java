    public void buildModuleBar() {
        ModuleMetadata rootModule = ClientSettings.getInstance().getRootModule();

        this.moduleBar.addModule(rootModule);
        for (int i = 0; i < rootModule.getChildCount(); i++) {
            ModuleMetadata folderMod = (ModuleMetadata) rootModule.getChildAt(i);
            for (int k = 0; k < folderMod.getChildCount(); k++) {
                this.moduleBar.addModule((ModuleMetadata) folderMod.getChildAt(k));
            }
        }
        this.moduleBar.doLayout();
        this.moduleBar.initializeHotKeys();

        // activate Desktop module
        this.moduleBar.actionPerformed(0);

    }

    @Override
    public void onEvent(Event e) {
        if (e instanceof AutoUpdateEvent) {

            this.lblUpdateStatus.setIcon(((AutoUpdateEvent) e).getSmallIcon());
            this.lblUpdateStatus.setToolTipText(((AutoUpdateEvent) e).getLongDescriptionHtml());
            this.lblUpdateStatus.addMouseListener(((AutoUpdateEvent) e).getMouseListener());
            this.lblUpdateStatus.setText(((AutoUpdateEvent) e).getDescription());
        } else if(e instanceof ServicesEvent) {
            this.mnuServices.removeAll();
            for (ServiceMenuItem si : ((ServicesEvent) e).getServices().values()) {
                if ("---".equals(si.getName())) {
                    this.mnuServices.add(new JSeparator());
                } else {
                    JMenuItem mi = new JMenuItem();
                    mi.setText(si.getName());
                    mi.setToolTipText(si.getTooltip());
                    mi.setIcon(new javax.swing.ImageIcon(getClass().getResource("/icons16/web.png")));
                    mi.addActionListener((ActionEvent ae) -> {
                        try {
                            DesktopUtils.openBrowser(si.getUrl());
                        } catch (Exception ex) {
                            log.error("Error opening service url", ex);
                            JOptionPane.showMessageDialog(EditorsRegistry.getInstance().getMainWindow(), "Service-Website kann nicht ge√∂ffnet werden: " + ex.getMessage(), "Browserfehler", JOptionPane.ERROR_MESSAGE);
                        }
                    });
                    this.mnuServices.add(mi);
                }
            }
        } else if (e instanceof NewsEvent) {
            this.lblNewsStatus.setIcon(((NewsEvent) e).getSmallIcon());
            this.lblNewsStatus.setToolTipText(((NewsEvent) e).getLongDescriptionHtml());
            this.lblNewsStatus.addMouseListener(((NewsEvent) e).getMouseListener());
            this.lblNewsStatus.setText(((NewsEvent) e).getDescription());
        } else if (e instanceof SystemStatusEvent) {

            Icon newIcon = null;
            String tooltip = "";
            String text = "";

            int level = ((SystemStatusEvent) e).getLevel();

            String monitoringCaption = "";
            switch (level) {
                case MonitoringConstants.LEVEL_ERROR:
                    newIcon = new javax.swing.ImageIcon(getClass().getResource("/icons/redled.png"));
                    monitoringCaption = java.util.ResourceBundle.getBundle("com/jdimension/jlawyer/client/editors/EditorsRegistry").getString("monitoring.error");
                    tooltip = "<html><p align=\"center\">" + monitoringCaption + "</p></html>";

                    break;
                case MonitoringConstants.LEVEL_WARNING:
                    newIcon = new javax.swing.ImageIcon(getClass().getResource("/icons/yellowled.png"));
                    monitoringCaption = java.util.ResourceBundle.getBundle("com/jdimension/jlawyer/client/editors/EditorsRegistry").getString("monitoring.warn");
                    tooltip = "<html><p align=\"center\">" + monitoringCaption + "</p></html>";

                    break;
                default:
                    newIcon = new javax.swing.ImageIcon(getClass().getResource("/icons/greenled.png"));
                    monitoringCaption = java.util.ResourceBundle.getBundle("com/jdimension/jlawyer/client/editors/EditorsRegistry").getString("monitoring.okay");
                    tooltip = "<html><p align=\"center\">" + monitoringCaption + "</p></html>";
                    break;
            }

            if (this.lblSystemStatus != null) {
                this.lblSystemStatus.setIcon(newIcon);
                this.lblSystemStatus.setText(text);
                this.lblSystemStatus.setToolTipText(tooltip);
            }
        } else if (e instanceof ScannerStatusEvent) {
            this.lblScanStatus.setIcon(new javax.swing.ImageIcon(getClass().getResource("/icons/scanner.png")));
            this.lblScanStatus.setText("" + ((ScannerStatusEvent) e).getFileNames().size());
            this.lblScanStatus.setToolTipText(((ScannerStatusEvent) e).getFileNames().size() + " " + java.util.ResourceBundle.getBundle("com/jdimension/jlawyer/client/editors/EditorsRegistry").getString("status.scansfound"));
            if (((ScannerStatusEvent) e).getFileNames().size() > 0) {
                this.lblScanStatus.setEnabled(true);
            } else {
                this.lblScanStatus.setEnabled(false);
            }
        } else if (e instanceof FaxStatusEvent) {

            this.lblFaxStatus.setText(" " + ((FaxStatusEvent) e).getFaxList().size());
            this.lblFaxStatus.setIcon(new javax.swing.ImageIcon(getClass().getResource("/icons16/kfax.png")));
            if (((FaxStatusEvent) e).getFailed() > 0) {

                String failedFaxesCaption = java.util.ResourceBundle.getBundle("com/jdimension/jlawyer/client/editors/EditorsRegistry").getString("caption.failedfaxes");
                this.lblFaxStatus.setToolTipText("<html><p align=\"center\">" + failedFaxesCaption + "</p></html>");
                this.lblFaxStatus.setForeground(DefaultColorTheme.COLOR_LOGO_RED);

            } else {
                this.lblFaxStatus.setToolTipText("");
                this.lblFaxStatus.setForeground(Color.black);

            }
            if (((FaxStatusEvent) e).getFaxList().size() > 0) {
                this.lblFaxStatus.setEnabled(true);
            } else {
                this.lblFaxStatus.setEnabled(false);
            }

        } else if (e instanceof EmailStatusEvent) {
            this.lblMailStatus.setIcon(new javax.swing.ImageIcon(getClass().getResource("/icons/folder_inbox.png")));
            this.lblMailStatus.setText("" + ((EmailStatusEvent) e).getUnread());
            this.lblMailStatus.setToolTipText(((EmailStatusEvent) e).getUnread() + " " + java.util.ResourceBundle.getBundle("com/jdimension/jlawyer/client/editors/EditorsRegistry").getString("status.unreadmails"));
            this.lblMailStatus.setEnabled(true);
        } else if (e instanceof BeaStatusEvent) {
            this.lblBeaStatus.setEnabled(true);
            if (((BeaStatusEvent) e).getUnread() > 0) {
                this.lblBeaStatus.setText("(*)");
                this.lblBeaStatus.setToolTipText("es gibt ungelesene beA-Nachrichten");
            } else {
                this.lblBeaStatus.setText("-");
                this.lblBeaStatus.setToolTipText("keine ungelesenen beA-Nachrichten");
            }
        } else if (e instanceof DrebisStatusEvent) {

            if (((DrebisStatusEvent) e).getMessages() > 0) {
                this.lblDrebisStatus.setEnabled(true);
                this.lblDrebisStatus.setText("" + ((DrebisStatusEvent) e).getMessages());
                this.lblDrebisStatus.setToolTipText("es gibt ungelesene Drebis-Nachrichten");
            } else {
                this.lblDrebisStatus.setText("-");
                this.lblDrebisStatus.setToolTipText("keine ungelesenen Drebis-Nachrichten");
            }
        }
    }

