    private boolean hasFileExtension(String s) {
        if (s == null) {
            return false;
        }
        s = s.toLowerCase();
        for (String ext : LauncherFactory.LO_OFFICEFILETYPES) {
            if (s.endsWith(ext.toLowerCase())) {
                return true;
            }
        }
        return false;
    }

