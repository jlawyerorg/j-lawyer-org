    private void addAndOpen(boolean openAfterAdd) {
        this.addedDocument = null;

        if (this.txtFileName.getText() == null || "".equals(this.txtFileName.getText()) || this.hasFileExtension(this.txtFileName.getText())) {
            JOptionPane.showMessageDialog(this, "Bitte geben Sie einen Dateinamen ohne Erweiterung ein.", "Dokument erstellen", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        ClientSettings settings = ClientSettings.getInstance();

        EditorsRegistry.getInstance().updateStatus("Erstelle Dokument...");
        ArchiveFileDocumentsBean db = null;
        try {
            JLawyerServiceLocator locator = JLawyerServiceLocator.getInstance(settings.getLookupProperties());
            HashMap<String, Object> phValues = new HashMap<>();
            TableModel model = this.tblPlaceHolders.getModel();
            for (int r = 0; r < model.getRowCount(); r++) {
                phValues.put(model.getValueAt(r, 0).toString(), model.getValueAt(r, 1));
            }

            DefaultMutableTreeNode tn = (DefaultMutableTreeNode) this.treeFolders.getSelectionPath().getLastPathComponent();
            GenericNode gn = (GenericNode) tn.getUserObject();

            String letterHead = "";
            if (this.cmbLetterHeads.getSelectedItem() != null) {
                letterHead = this.cmbLetterHeads.getSelectedItem().toString();
            }

            db = locator.lookupArchiveFileServiceRemote().addDocumentFromTemplate(this.aFile.getId(), this.txtFileName.getText(), letterHead, gn, this.lstTemplates.getSelectedValue().toString(), phValues, this.cmbDictateSigns.getSelectedItem().toString(), null);
            this.addedDocument = db;
            targetTable.addDocument(db, this.invoice);

        } catch (Exception ex) {
            log.error("Error adding document from template " + this.lstTemplates.getSelectedValue().toString(), ex);
            JOptionPane.showMessageDialog(this, "Fehler beim Hinzufügen des Dokuments: " + ex.getMessage(), com.jdimension.jlawyer.client.utils.DesktopUtils.POPUP_TITLE_ERROR, JOptionPane.ERROR_MESSAGE);
            EditorsRegistry.getInstance().clearStatus();
            return;
        }

        if (this.invoice != null) {
            try {
                JLawyerServiceLocator locator = JLawyerServiceLocator.getInstance(settings.getLookupProperties());
                locator.lookupArchiveFileServiceRemote().linkInvoiceDocument(db.getId(), this.invoice.getId());
            } catch (Exception ex) {
                log.error("Error linking invoice document", ex);
                JOptionPane.showMessageDialog(this, "Fehler beim Verknüpfen von Rechnung und Dokument: " + ex.getMessage(), com.jdimension.jlawyer.client.utils.DesktopUtils.POPUP_TITLE_ERROR, JOptionPane.ERROR_MESSAGE);
                EditorsRegistry.getInstance().clearStatus();
                return;
            }
        }

        if (!(this.radioReviewTypeNone.isSelected())) {
            if (this.txtReviewDateField.getText().length() != 10) {
                JOptionPane.showMessageDialog(this, "Wiedervorlagedatum ungültig", "Dokument erstellen", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            Date d = null;
            try {
                SimpleDateFormat df = new SimpleDateFormat("dd.MM.yyyy");
                d = df.parse(this.txtReviewDateField.getText());
            } catch (Throwable t) {
                JOptionPane.showMessageDialog(this, "Wiedervorlagedatum ungültig", "Dokument erstellen", JOptionPane.INFORMATION_MESSAGE);
                return;
            }

            ArchiveFileReviewsBean reviewDto = new ArchiveFileReviewsBean();
            reviewDto.setEventType(ArchiveFileReviewsBean.EVENTTYPE_FOLLOWUP);
            if (this.radioReviewTypeRespite.isSelected()) {
                reviewDto.setEventType(ArchiveFileReviewsBean.EVENTTYPE_RESPITE);
            }
            reviewDto.setDone(false);
            reviewDto.setBeginDate(d);
            reviewDto.setAssignee(this.cmbReviewAssignee.getSelectedItem().toString());
            reviewDto.setSummary(this.cmbReviewReason.getEditor().getItem().toString());
            reviewDto.setCalendarSetup(this.calendarSelectionButton1.getSelectedSetup());

            if (CalendarUtils.checkForConflicts(this, reviewDto)) {
                try {
                    CalendarUtils.getInstance().storeCalendarEntry(reviewDto, this.aFile, (CalendarEntryTemplate) this.cmbReviewReason.getItemAt(this.cmbReviewReason.getSelectedIndex()), this);
                } catch (Exception ex) {
                    log.error("Error adding review", ex);
                    JOptionPane.showMessageDialog(this, "Fehler beim Speichern des Kalendereintrages: " + ex.getMessage(), com.jdimension.jlawyer.client.utils.DesktopUtils.POPUP_TITLE_ERROR, JOptionPane.ERROR_MESSAGE);
                    return;
                }
            }

        }

        if (this.chkGeneratePDF.isSelected() && this.casePanel != null) {
            EditorsRegistry.getInstance().updateStatus("Dokument wird in PDF konviertiert...");
            try {
                this.casePanel.convertDocumentToPdf(db);
            } catch (Exception ex) {
                log.error("Error converting to PDF", ex);
                JOptionPane.showMessageDialog(this, "Fehler bei der PDF-Konvertierung: " + ex.getMessage(), com.jdimension.jlawyer.client.utils.DesktopUtils.POPUP_TITLE_ERROR, JOptionPane.ERROR_MESSAGE);
            }
            EditorsRegistry.getInstance().updateStatus("PDF-Konvertierung abgeschlossen.", 5000);
        }

        if (openAfterAdd) {
            try {
                CaseUtils.openDocument(aFile, db, false, this);
            } catch (Exception ex) {
                log.error("Error opening document", ex);
                JOptionPane.showMessageDialog(this, "Dokument kann nicht geöffnet werden: " + ex.getMessage(), com.jdimension.jlawyer.client.utils.DesktopUtils.POPUP_TITLE_ERROR, JOptionPane.ERROR_MESSAGE);
            }
        }

        EditorsRegistry.getInstance().clearStatus();
        this.setVisible(false);
        this.dispose();
    }

