    @Override
    public boolean save() {
        if ((this.txtName.getText() == null || "".equals(this.txtName.getText())) && (this.txtCompany.getText() == null || "".equals(this.txtCompany.getText()))) {
            JOptionPane.showMessageDialog(this, "Es muss mindestens ein Name oder ein Firmenname angegeben werden, um eine Adresse zu speichern.", "Adressen - Gültigkeitsprüfung", JOptionPane.INFORMATION_MESSAGE);
            return false;
        }

        ClientSettings settings = ClientSettings.getInstance();
        EditorsRegistry.getInstance().updateStatus("Adresse wird gespeichert...");
        try {
            JLawyerServiceLocator locator = JLawyerServiceLocator.getInstance(settings.getLookupProperties());
            AddressServiceRemote addressService = locator.lookupAddressServiceRemote();

            String id = null;
            String externalId1 = null;
            String externalId2 = null;
            String externalId3 = null;
            String externalId4 = null;
            String externalId5 = null;
            if (this.dto != null) {
                id = this.dto.getId();
                externalId1 = this.dto.getExternalId1();
                externalId2 = this.dto.getExternalId2();
                externalId3 = this.dto.getExternalId3();
                externalId4 = this.dto.getExternalId4();
                externalId5 = this.dto.getExternalId5();
            }

            this.dto = new AddressBean();
            if (id != null) {
                this.dto.setId(id);
            }
            if (externalId1 != null) {
                this.dto.setExternalId1(externalId1);
            }
            if (externalId2 != null) {
                this.dto.setExternalId2(externalId2);
            }
            if (externalId3 != null) {
                this.dto.setExternalId3(externalId3);
            }
            if (externalId4 != null) {
                this.dto.setExternalId4(externalId4);
            }
            if (externalId5 != null) {
                this.dto.setExternalId5(externalId5);
            }

            this.fillDTO(this.dto);

            if (id == null) {
                List<AddressBean> similarAddresses = addressService.similaritySearch(dto, 0.85f);
                if (!similarAddresses.isEmpty()) {
                    StringBuilder html = new StringBuilder();
                    html.append("<html>Es wurden &auml;hnliche Eintr&auml;ge gefunden - trotzdem speichern?<br/>");
                    html.append("<ul>");
                    for (AddressBean s : similarAddresses) {
                        html.append("<li>").append(s.toShortHtml(false)).append("</li>");
                    }
                    html.append("</ul>");
                    html.append("</html>");
                    int response = JOptionPane.showConfirmDialog(this, html.toString(), "Ähnlichkeitssuche", JOptionPane.YES_NO_OPTION);
                    if (response == JOptionPane.NO_OPTION) {
                        return true;
                    }
                }
                addressService.createAddress(this.dto);
                
            } else {
                addressService.updateAddress(this.dto);
                EventBroker eb = EventBroker.getInstance();
                eb.publishEvent(new ContactUpdatedEvent(this.dto));
            }
            this.lblHeaderInfo.setText(this.dto.toDisplayName());

            // we need to update the search tables because they pass their cached DTOs to the editors
            // without updating, they would still have the old invalid data
            try {
                Object editor = EditorsRegistry.getInstance().getEditor(EditAddressPanel.class.getName());
                ((QuickAddressSearchPanel) editor).updateTable();

            } catch (Exception ex) {
                log.error("Error creating editor from class " + this.openedFromEditorClass, ex);
                JOptionPane.showMessageDialog(this, "Fehler beim Laden des Editors: " + ex.getMessage(), com.jdimension.jlawyer.client.utils.DesktopUtils.POPUP_TITLE_ERROR, JOptionPane.ERROR_MESSAGE);
            }

            if (id == null) {
                // it's a new adress, so clear the input fields when saved
                this.clearInputs();
            }

            EditorsRegistry.getInstance().updateStatus("Adresse gespeichert.", 5000);

        } catch (Exception ex) {
            log.error("Error saving address", ex);
            JOptionPane.showMessageDialog(this, "Fehler beim Speichern: " + ex.getMessage(), com.jdimension.jlawyer.client.utils.DesktopUtils.POPUP_TITLE_ERROR, JOptionPane.ERROR_MESSAGE);
            EditorsRegistry.getInstance().clearStatus();
            return false;
        }
        return true;
    }

