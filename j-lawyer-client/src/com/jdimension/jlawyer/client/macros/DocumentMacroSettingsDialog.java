    private class DocumentTableModel extends AbstractTableModel {

        private final String[] columns = {"Ordner", "Vorlage", "Dateiname", "Briefkopf", "Je Beteiligtem", "Beteiligten-Typ", "PDF", "Bed."};
        private List<DocumentMacro.DocumentStep> steps = new ArrayList<>();

        public void setSteps(List<DocumentMacro.DocumentStep> steps) {
            this.steps = new ArrayList<>(steps);
            fireTableDataChanged();
        }

        public List<DocumentMacro.DocumentStep> getSteps() {
            return new ArrayList<>(steps);
        }

        public void addStep(DocumentMacro.DocumentStep step) {
            steps.add(step);
            fireTableRowsInserted(steps.size() - 1, steps.size() - 1);
        }

        public void removeRow(int row) {
            if (row >= 0 && row < steps.size()) {
                steps.remove(row);
                fireTableRowsDeleted(row, row);
            }
        }

        public DocumentMacro.DocumentStep getStepAt(int row) {
            return steps.get(row);
        }

        public void fireRowUpdated(int row) {
            fireTableRowsUpdated(row, row);
        }

        public void setTemplate(int row, String folder, String template) {
            DocumentMacro.DocumentStep step = steps.get(row);
            step.setFolderPath(folder);
            step.setTemplateName(template);
            if (step.getFileNameTemplate() == null || step.getFileNameTemplate().trim().isEmpty()) {
                step.setFileNameTemplate("{DATUM.heute_iso}_" + template.replaceAll("\\.[^.]+$", ""));
            }
            fireTableRowsUpdated(row, row);
            warnIfDocumentFileNameAlreadyExists(row);
        }

        @Override
        public int getRowCount() {
            return steps.size();
        }

        @Override
        public int getColumnCount() {
            return columns.length;
        }

        @Override
        public String getColumnName(int column) {
            return columns[column];
        }

        @Override
        public boolean isCellEditable(int rowIndex, int columnIndex) {
            return columnIndex != 7 && (columnIndex != 5 || steps.get(rowIndex).isCreatePerParty());
        }

        @Override
        public Object getValueAt(int row, int col) {
            DocumentMacro.DocumentStep step = steps.get(row);
            switch (col) {
                case 0:
                    return step.getFolderPath();
                case 1:
                    return step.getTemplateName();
                case 2:
                    return step.getFileNameTemplate();
                case 3:
                    return step.getLetterHead();
                case 4:
                    return step.isCreatePerParty();
                case 5:
                    return step.getPartyType();
                case 6:
                    return step.isCreatePdf();
                case 7:
                    return step.getConditions() == null ? 0 : step.getConditions().getRules().size();
                default:
                    return "";
            }
        }

        @Override
        public void setValueAt(Object value, int row, int col) {
            DocumentMacro.DocumentStep step = steps.get(row);
            switch (col) {
                case 0:
                    step.setFolderPath(String.valueOf(value));
                    break;
                case 1:
                    step.setTemplateName(String.valueOf(value));
                    break;
                case 2:
                    step.setFileNameTemplate(String.valueOf(value));
                    warnIfDocumentFileNameAlreadyExists(row);
                    break;
                case 3:
                    step.setLetterHead(String.valueOf(value));
                    break;
                case 4:
                    step.setCreatePerParty(Boolean.parseBoolean(String.valueOf(value)));
                    if (step.isCreatePerParty() && (step.getPartyType() == null || step.getPartyType().trim().isEmpty()) && !partyTypeOptions.isEmpty()) {
                        step.setPartyType(partyTypeOptions.get(0).getValue());
                    }
                    break;
                case 5:
                    step.setPartyType(valueOfEditorValue(value));
                    break;
                case 6:
                    step.setCreatePdf(Boolean.parseBoolean(String.valueOf(value)));
                    break;
                case 7:
                    break;
                default:
                    break;
            }
        }

        @Override
        public Class<?> getColumnClass(int columnIndex) {
            if (columnIndex == 4) {
                return Boolean.class;
            }
            if (columnIndex == 6) {
                return Boolean.class;
            }
            if (columnIndex == 7) {
                return Integer.class;
            }
            return String.class;
        }
    }

    private class DueDateTableModel extends AbstractTableModel {

        private final String[] columns = {"Typ", "Titel", "Beschreibung", "Versatz", "Einheit", "Bearbeiter", "Kalender", "Beginn", "Ende", "Erinnerung", "Bed."};
        private List<DocumentMacro.DueDateStep> steps = new ArrayList<>();

        public void setSteps(List<DocumentMacro.DueDateStep> steps) {
            this.steps = new ArrayList<>(steps);
            for (DocumentMacro.DueDateStep step : this.steps) {
                applyClientTimeDefaults(step);
                if (!isCalendarCompatibleWithEventType(step.getCalendar(), step.getEventType())) {
                    step.setCalendar(defaultCalendarForEventType(step.getEventType()));
                }
            }
            fireTableDataChanged();
        }

        public List<DocumentMacro.DueDateStep> getSteps() {
            return new ArrayList<>(steps);
        }

        public void addStep(DocumentMacro.DueDateStep step) {
            applyClientTimeDefaults(step);
            if (!isCalendarCompatibleWithEventType(step.getCalendar(), step.getEventType())) {
                step.setCalendar(defaultCalendarForEventType(step.getEventType()));
            }
            steps.add(step);
            fireTableRowsInserted(steps.size() - 1, steps.size() - 1);
        }

        public void removeRow(int row) {
            if (row >= 0 && row < steps.size()) {
                steps.remove(row);
                fireTableRowsDeleted(row, row);
            }
        }

        public DocumentMacro.DueDateStep getStepAt(int row) {
            return steps.get(row);
        }

        public void fireRowUpdated(int row) {
            fireTableRowsUpdated(row, row);
        }

        public int getEventTypeAt(int row) {
            if (row >= 0 && row < steps.size()) {
                return steps.get(row).getEventType();
            }
            return ArchiveFileReviewsBean.EVENTTYPE_FOLLOWUP;
        }

        @Override
        public int getRowCount() {
            return steps.size();
        }

        @Override
        public int getColumnCount() {
            return columns.length;
        }

        @Override
        public String getColumnName(int column) {
            return columns[column];
        }

        @Override
        public boolean isCellEditable(int rowIndex, int columnIndex) {
            if (columnIndex == 10) {
                return false;
            }
            if (columnIndex == 7 || columnIndex == 8 || columnIndex == 9) {
                return steps.get(rowIndex).getEventType() == ArchiveFileReviewsBean.EVENTTYPE_EVENT;
            }
            return true;
        }

        @Override
        public Object getValueAt(int row, int col) {
            DocumentMacro.DueDateStep step = steps.get(row);
            switch (col) {
                case 0:
                    return String.valueOf(step.getEventType());
                case 1:
                    return step.getSummaryTemplate();
                case 2:
                    return step.getDescriptionTemplate();
                case 3:
                    return step.getOffsetAmount();
                case 4:
                    return step.getOffsetUnit();
                case 5:
                    return step.getAssignee();
                case 6:
                    return step.getCalendar();
                case 7:
                    return step.getBeginTime();
                case 8:
                    return step.getEndTime();
                case 9:
                    return step.getReminderMinutes();
                case 10:
                    return step.getConditions() == null ? 0 : step.getConditions().getRules().size();
                default:
                    return "";
            }
        }

        @Override
        public void setValueAt(Object value, int row, int col) {
            DocumentMacro.DueDateStep step = steps.get(row);
            switch (col) {
                case 0:
                    int previousEventType = step.getEventType();
                    int eventType = parseEventType(value);
                    step.setEventType(eventType);
                    String currentCalendar = step.getCalendar();
                    if (!isCalendarCompatibleWithEventType(currentCalendar, eventType)) {
                        step.setCalendar(defaultCalendarForEventType(eventType));
                    }
                    if (previousEventType != eventType) {
                        applyClientTimeDefaults(step);
                    }
                    fireTableRowsUpdated(row, row);
                    break;
                case 1:
                    step.setSummaryTemplate(String.valueOf(value));
                    break;
                case 2:
                    step.setDescriptionTemplate(String.valueOf(value));
                    break;
                case 3:
                    step.setOffsetAmount(DocumentMacro.asInt(value, 0));
                    break;
                case 4:
                    step.setOffsetUnit(valueOfEditorValue(value));
                    break;
                case 5:
                    step.setAssignee(valueOfEditorValue(value));
                    break;
                case 6:
                    step.setCalendar(valueOfEditorValue(value));
                    break;
                case 7:
                    step.setBeginTime(String.valueOf(value));
                    break;
                case 8:
                    step.setEndTime(String.valueOf(value));
                    break;
                case 9:
                    step.setReminderMinutes(DocumentMacro.asInt(value, 0));
                    break;
                case 10:
                    break;
                default:
                    break;
            }
        }

        @Override
        public Class<?> getColumnClass(int columnIndex) {
            return columnIndex == 10 ? Integer.class : String.class;
        }
    }
