    private List<ValueLabelOption> buildCalendarOptions(int eventType, String currentValue) {
        List<ValueLabelOption> options = new ArrayList<>();
        for (CalendarSetup setup : calendarSetups) {
            if (setup.getEventType() == eventType) {
                options.add(new ValueLabelOption(setup.getId(), getCalendarLabel(setup)));
            }
        }
        if (options.isEmpty()) {
            options.add(new ValueLabelOption("", "Kein Kalender für " + eventTypeLabel(eventType) + " verfügbar"));
        }
        return options;
    }

    private boolean isCalendarCompatibleWithEventType(String calendarValue, int eventType) {
        if (calendarValue == null || calendarValue.trim().isEmpty()) {
            return false;
        }
        for (CalendarSetup setup : calendarSetups) {
            if (setup.getEventType() == eventType
                    && (calendarValue.equals(setup.getId()) || calendarValue.equalsIgnoreCase(nonEmpty(setup.getDisplayName(), "")))) {
                return true;
            }
        }
        return false;
    }

    private String defaultCalendarForEventType(int eventType) {
        for (CalendarSetup setup : calendarSetups) {
            if (setup.getEventType() == eventType) {
                return setup.getId();
            }
        }
        return "";
    }

