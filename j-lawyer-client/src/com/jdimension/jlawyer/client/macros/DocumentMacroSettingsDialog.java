    private static int parseEventType(Object value) {
        String text = valueOfEditorValue(value);
        if (String.valueOf(ArchiveFileReviewsBean.EVENTTYPE_RESPITE).equals(text) || "Frist".equalsIgnoreCase(text)) {
            return ArchiveFileReviewsBean.EVENTTYPE_RESPITE;
        }
        if (String.valueOf(ArchiveFileReviewsBean.EVENTTYPE_EVENT).equals(text) || "Termin".equalsIgnoreCase(text)) {
            return ArchiveFileReviewsBean.EVENTTYPE_EVENT;
        }
        return ArchiveFileReviewsBean.EVENTTYPE_FOLLOWUP;
    }

    private static void applyClientTimeDefaults(DocumentMacro.DueDateStep step) {
        if (step.getEventType() == ArchiveFileReviewsBean.EVENTTYPE_EVENT) {
            if (step.getBeginTime() == null || step.getBeginTime().trim().isEmpty() || "00:00".equals(step.getBeginTime())) {
                step.setBeginTime("10:00");
            }
            if (step.getEndTime() == null || step.getEndTime().trim().isEmpty() || "23:59".equals(step.getEndTime())) {
                step.setEndTime("11:00");
            }
            if (step.getReminderMinutes() < 0) {
                step.setReminderMinutes(0);
            }
            return;
        }
        step.setBeginTime("00:00");
        step.setEndTime("23:59");
        step.setReminderMinutes(-1);
    }

    private static String valueOfEditorValue(Object value) {
        if (value == null) {
            return "";
        }
        if (value instanceof ValueLabelOption) {
            return ((ValueLabelOption) value).getValue();
        }
        return String.valueOf(value);
    }

