    private void loadDueDateLookups() {
        assigneeOptions.clear();
        partyTypeOptions.clear();
        calendarSetups.clear();
        assigneeOptions.add(new ValueLabelOption("lawyer", "Aktenanwalt"));
        assigneeOptions.add(new ValueLabelOption("assistant", "Sachbearbeiter"));
        assigneeOptions.add(new ValueLabelOption("current", "Aktueller Benutzer"));

        try {
            JLawyerServiceLocator locator = JLawyerServiceLocator.getInstance(ClientSettings.getInstance().getLookupProperties());
            List<PartyTypeBean> partyTypes = locator.lookupSystemManagementRemote().getPartyTypes();
            if (partyTypes != null) {
                Collections.sort(partyTypes, new Comparator<PartyTypeBean>() {
                    @Override
                    public int compare(PartyTypeBean o1, PartyTypeBean o2) {
                        return nonEmpty(o1.getName(), "").compareToIgnoreCase(nonEmpty(o2.getName(), ""));
                    }
                });
                for (PartyTypeBean partyType : partyTypes) {
                    if (partyType != null && partyType.getName() != null && !partyType.getName().trim().isEmpty()) {
                        partyTypeOptions.add(new ValueLabelOption(partyType.getName(), partyType.getName()));
                    }
                }
            }

            List<AppUserBean> users = locator.lookupSystemManagementRemote().getUsers();
            if (users != null) {
                Collections.sort(users, new Comparator<AppUserBean>() {
                    @Override
                    public int compare(AppUserBean o1, AppUserBean o2) {
                        return getUserLabel(o1).compareToIgnoreCase(getUserLabel(o2));
                    }
                });
                for (AppUserBean user : users) {
                    if (user != null && user.getPrincipalId() != null && !user.getPrincipalId().trim().isEmpty()) {
                        assigneeOptions.add(new ValueLabelOption(user.getPrincipalId(), getUserLabel(user)));
                    }
                }
            }

            List<CalendarSetup> setups = locator.lookupCalendarServiceRemote().getAllCalendarSetups();
            if (setups != null) {
                calendarSetups.addAll(setups);
            }
            Collections.sort(calendarSetups, new Comparator<CalendarSetup>() {
                @Override
                public int compare(CalendarSetup o1, CalendarSetup o2) {
                    return getCalendarLabel(o1).compareToIgnoreCase(getCalendarLabel(o2));
                }
            });
        } catch (Exception ex) {
            log.error("Could not load document macro due date lookup values", ex);
            JOptionPane.showMessageDialog(this, "Bearbeiter und Kalender konnten nicht geladen werden: " + getMessage(ex), "Dokumentenmakros", JOptionPane.ERROR_MESSAGE);
        }
    }

