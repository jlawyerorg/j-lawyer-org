    private static String getUserLabel(AppUserBean user) {
        if (user == null) {
            return "";
        }
        String principalId = nonEmpty(user.getPrincipalId(), "");
        String displayName = nonEmpty(user.getDisplayName(), principalId);
        if (principalId.isEmpty() || principalId.equals(displayName)) {
            return displayName;
        }
        return displayName + " (" + principalId + ")";
    }

    private static String getCalendarLabel(CalendarSetup setup) {
        if (setup == null) {
            return "";
        }
        return nonEmpty(setup.getDisplayName(), setup.getId()) + " (" + eventTypeLabel(setup.getEventType()) + ")";
    }

    private static String getAssigneeLabel(String value, List<ValueLabelOption> options) {
        return getOptionLabel(value, options);
    }

    private static String getCalendarLabel(String value, List<CalendarSetup> setups) {
        if (value == null || value.trim().isEmpty()) {
            return "";
        }
        for (CalendarSetup setup : setups) {
            if (value.equals(setup.getId())) {
                return getCalendarLabel(setup);
            }
        }
        return "";
    }

    private static String getOptionLabel(String value, List<ValueLabelOption> options) {
        if (value == null || value.trim().isEmpty()) {
            return "";
        }
        for (ValueLabelOption option : options) {
            if (value.equals(option.getValue())) {
                return option.getLabel();
            }
        }
        return value;
    }

    private static String nonEmpty(String value, String fallback) {
        return value == null || value.trim().isEmpty() ? fallback : value;
    }

