    private static class ConditionMatchResult {

        private final boolean matches;
        private final List<String> failedRules;

        private ConditionMatchResult(boolean matches, List<String> failedRules) {
            this.matches = matches;
            this.failedRules = failedRules == null ? new ArrayList<>() : failedRules;
        }

        static ConditionMatchResult match() {
            return new ConditionMatchResult(true, new ArrayList<>());
        }

        static ConditionMatchResult noMatch(List<String> failedRules) {
            return new ConditionMatchResult(false, failedRules);
        }

        boolean matches() {
            return matches;
        }

        String getDescription() {
            if (failedRules.isEmpty()) {
                return "keine passende Bedingung";
            }
            return String.join("; ", failedRules);
        }
    }

