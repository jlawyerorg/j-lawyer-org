    private CalendarSetup resolveCalendar(DocumentMacro.DueDateStep step) throws Exception {
        JLawyerServiceLocator locator = JLawyerServiceLocator.getInstance(ClientSettings.getInstance().getLookupProperties());
        CalendarServiceRemote calendarService = locator.lookupCalendarServiceRemote();
        List<CalendarSetup> calendars;
        try {
            calendars = calendarService.getAllCalendarSetups();
        } catch (Exception ex) {
            log.warn("Could not load all calendar setups, falling back to current user's calendars", ex);
            calendars = calendarService.getCalendarSetupsForUser(UserSettings.getInstance().getCurrentUser().getPrincipalId());
        }

        String calendarValue = nonEmpty(step.getCalendar(), "");
        for (CalendarSetup setup : calendars) {
            if (setup.getEventType() == step.getEventType()
                    && (calendarValue.equals(setup.getId()) || calendarValue.equalsIgnoreCase(nonEmpty(setup.getDisplayName(), "")))) {
                return setup;
            }
        }
        return null;
    }

    private String resolvePlaceholders(String text) {
        String result = nonEmpty(text, "");
        SimpleDateFormat de = new SimpleDateFormat("dd.MM.yyyy");
        SimpleDateFormat iso = new SimpleDateFormat("yyyy-MM-dd");
        Calendar now = Calendar.getInstance();
        result = result.replace("{AKTE.name}", nonEmpty(caseBean.getName(), ""));
        result = result.replace("{AKTE.reason}", nonEmpty(caseBean.getReason(), ""));
        result = result.replace("{AKTE.subjectField}", nonEmpty(caseBean.getSubjectField(), ""));
        result = result.replace("{AKTE.fileNumber}", nonEmpty(caseBean.getFileNumber(), ""));
        result = result.replace("{AKTE.id}", nonEmpty(caseBean.getId(), ""));
        result = result.replace("{AKTE.lawyer}", nonEmpty(caseBean.getLawyer(), ""));
        result = result.replace("{AKTE.assistant}", nonEmpty(caseBean.getAssistant(), ""));
        result = result.replace("{DATUM.heute}", de.format(now.getTime()));
        result = result.replace("{DATUM.heute_iso}", iso.format(now.getTime()));
        result = result.replace("{DATUM.jahr}", String.valueOf(now.get(Calendar.YEAR)));
        result = result.replace("{DATUM.monat}", String.format("%02d", now.get(Calendar.MONTH) + 1));
        result = result.replace("{DATUM.tag}", String.format("%02d", now.get(Calendar.DATE)));
        AppUserBean currentUser = UserSettings.getInstance().getCurrentUser();
        String currentUserName = currentUser == null ? "" : nonEmpty(currentUser.getDisplayName(), currentUser.getPrincipalId());
        result = result.replace("{USER.current}", currentUserName);
        return result;
    }

    private String nonEmpty(String value, String fallback) {
        return value == null || value.trim().isEmpty() ? fallback : value;
    }

