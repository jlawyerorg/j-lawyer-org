    class RecipientsActionListener implements java.awt.event.ActionListener {

        private String safeId = null;
        private JList to = null;
        private JDialog caller = null;

        public RecipientsActionListener(JDialog caller, String safeId, JList to) {
            this.safeId = safeId;
            this.to = to;
            this.caller = caller;
        }

        @Override
        public void actionPerformed(ActionEvent e) {
            DefaultListModel model = (DefaultListModel) this.to.getModel();

            try {
                BeaAccess bea = BeaAccess.getInstance();
                JCheckBoxMenuItem mi = (JCheckBoxMenuItem) e.getSource();
                Identity i = bea.getIdentity(safeId);
                if (mi.getState()) {

                    if (!model.contains(i)) {
                        model.addElement(i);
                    }

                } else if (model.contains(i)) {
                    model.removeElement(i);
                }
            } catch (Throwable t) {
                log.error(t);
                JOptionPane.showMessageDialog(this.caller, "Fehler: " + t.getMessage(), com.jdimension.jlawyer.client.utils.DesktopUtils.POPUP_TITLE_ERROR, JOptionPane.ERROR_MESSAGE);
            }
        }
    }
