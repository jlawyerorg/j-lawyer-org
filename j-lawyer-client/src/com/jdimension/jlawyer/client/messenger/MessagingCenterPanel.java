    @Override
    public void newMessageForSubmission(InstantMessage msg) {

        int tabIndex=this.tabsPane.getSelectedIndex();
        // preserve case context of the current tab
        if(tabIndex > 2) {
            JScrollPane sp = (JScrollPane) this.tabsPane.getComponentAt(tabIndex);
            
            Object caseContext=sp.getClientProperty(CLIENTPROPERTY_CASECONTEXT);
            if(caseContext!=null) {
                msg.setCaseContext((ArchiveFileBean)caseContext);
            }
            
        }
        
        
        try {

            ClientSettings settings = ClientSettings.getInstance();
            JLawyerServiceLocator locator = JLawyerServiceLocator.getInstance(settings.getLookupProperties());

            msg = locator.lookupMessagingServiceRemote().submitMessage(msg);

        } catch (Exception ex) {
            log.error("Could not submit instant message to server", ex);
            JOptionPane.showMessageDialog(EditorsRegistry.getInstance().getMainWindow(), "Nachricht konnte nicht gesendet werden: " + ex.getMessage(), com.jdimension.jlawyer.client.utils.DesktopUtils.POPUP_TITLE_ERROR, JOptionPane.ERROR_MESSAGE);
        }
    }

    @Override
    public void onEvent(Event e) {
        if (e instanceof NewInstantMessagesEvent) {

            SwingUtilities.invokeLater(() -> {
                if (((NewInstantMessagesEvent) e).getNewMessages() != null) {
                    for (InstantMessage msg : ((NewInstantMessagesEvent) e).getNewMessages()) {
                        this.currentMessageList.add(msg);
                        this.addMessageToView(msg);
                    }
                    this.updateHashtagsBar(((NewInstantMessagesEvent) e).getNewMessages());
                }
            });
        } else if (e instanceof InstantMessageMentionChangedEvent) {
            for (int i = 0; i < this.tabsPane.getTabCount(); i++) {
                JScrollPane sp = (JScrollPane) this.tabsPane.getComponentAt(i);
                this.updateMentionStatus(((JPanel) sp.getViewport().getComponent(0)), (InstantMessageMentionChangedEvent) e);
            }
            
        } else if (e instanceof InstantMessageDeletedEvent) {
            this.deletedMessageIds.add(((InstantMessageDeletedEvent) e).getMessageId());
            
            for (int i = 0; i < this.tabsPane.getTabCount(); i++) {
                JScrollPane sp = (JScrollPane) this.tabsPane.getComponentAt(i);
                this.deleteMessage(((JPanel) sp.getViewport().getComponent(0)), (InstantMessageDeletedEvent) e);
            }
            
        }
    }

    private void deleteMessage(JPanel messagesPanel, InstantMessageDeletedEvent e) {
        boolean removed=false;
        for (int i = 0; i < messagesPanel.getComponentCount(); i++) {
            if (messagesPanel.getComponent(i) instanceof MessagePanel) {
                if (((MessagePanel) messagesPanel.getComponent(i)).getMessage().getId().equals(e.getMessageId())) {
                    messagesPanel.remove(messagesPanel.getComponent(i));
                    removed=true;
                    break;
                }
            }
        }
        if(removed) {
            messagesPanel.revalidate();
            messagesPanel.doLayout();
        }
    }

    private void updateMentionStatus(JPanel messagesPanel, InstantMessageMentionChangedEvent e) {
        for (int i = 0; i < messagesPanel.getComponentCount(); i++) {
            if (messagesPanel.getComponent(i) instanceof MessagePanel) {
                ((MessagePanel) messagesPanel.getComponent(i)).mentionUpdated(e.getMessageId(), e.getMentionId(), e.isDone());
            }
        }
    }
