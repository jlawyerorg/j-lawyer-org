    private void addMessageToView(InstantMessage msg) {
        MessagePanel mp1 = new MessagePanel(UserSettings.getInstance().getLoginEnabledUsers(), UserSettings.getInstance().getCurrentUser().getPrincipalId(), UserSettings.getInstance().getCurrentUser().getPrincipalId().equalsIgnoreCase(msg.getSender()), msg);
        mp1.setAlignmentX(JPanel.LEFT_ALIGNMENT);
        pnlMessages.add(mp1);
        pnlMessages.repaint();
        this.jScrollPane3.doLayout();
        mp1.revalidate();
        this.jScrollPane3.revalidate();

        JScrollBar vertical = this.jScrollPane3.getVerticalScrollBar();
        vertical.setValue(vertical.getMaximum());

        AdjustmentListener downScroller = new AdjustmentListener() {
            @Override
            public void adjustmentValueChanged(AdjustmentEvent e) {
                Adjustable adjustable = e.getAdjustable();
                adjustable.setValue(adjustable.getMaximum());
                vertical.removeAdjustmentListener(this);
            }
        };
        vertical.addAdjustmentListener(downScroller);

        this.addToTabIfRequired(msg);
        this.addToMentioningTabsIfRequired(msg);
    }

    private void addToTabIfRequired(InstantMessage msg) {

        int selectedTabIndex=this.tabsPane.getSelectedIndex();
        String selectedTabCaption=null;
        if(selectedTabIndex>-1)
            selectedTabCaption=this.tabsPane.getTitleAt(selectedTabIndex);
        
        if (msg.getCaseContext() == null) {
            return;
        }

        int existingTab = -1;
        for (int i = 0; i < this.tabsPane.getTabCount(); i++) {
            Object caseContext=((JScrollPane)tabsPane.getComponentAt(i)).getClientProperty(CLIENTPROPERTY_CASECONTEXT);
            if(caseContext!=null && caseContext instanceof ArchiveFileBean) {
                if(((ArchiveFileBean)caseContext).getId().equals(msg.getCaseContext().getId())) {
                    existingTab = i;
                    break;
                }
            }
        }

        JPanel targetPanel = null;
        JScrollPane targetScroll = null;
        if (existingTab < 0) {
            targetScroll = new JScrollPane();
            targetScroll.getVerticalScrollBar().setUnitIncrement(SCROLLPANE_VERTICAL_INCREMENT);

            targetPanel = new JPanel();
            BoxLayout layout = new javax.swing.BoxLayout(targetPanel, javax.swing.BoxLayout.Y_AXIS);
            targetPanel.setLayout(layout);
            targetPanel.setOpaque(false);

            targetScroll.getViewport().add(targetPanel);
            targetScroll.getViewport().setOpaque(false);
            targetScroll.setBorder(null);
            targetScroll.setOpaque(false);
            targetScroll.putClientProperty(CLIENTPROPERTY_CASECONTEXT, msg.getCaseContext());
            tabsPane.addTab(getTabTitleForMessage(msg), targetScroll);
            tabsPane.setToolTipTextAt(tabsPane.getTabCount()-1, this.getTooltipForTab(msg.getCaseContext()));
            existingTab = tabsPane.getTabCount() - 1;

        } else {
            targetScroll = (JScrollPane) tabsPane.getComponentAt(existingTab);
            targetPanel = (JPanel) targetScroll.getViewport().getComponent(0);
        }

        // see if the first case-related tab has a message older than this one
        // if so, move the current tab to the the left
        if (tabsPane.getTabCount() > 3 && existingTab != 3) {
            JScrollPane tab4Scroll = (JScrollPane) tabsPane.getComponentAt(4);
            JPanel tab4Panel = (JPanel) tab4Scroll.getViewport().getComponent(0);
            if (tab4Panel.getComponentCount() > 0) {
                MessagePanel mp = (MessagePanel) tab4Panel.getComponent(tab4Panel.getComponentCount() - 1);
                Date tab4latest = mp.getMessage().getSent();
                if (msg.getSent().getTime() > tab4latest.getTime()) {
                    // move tab to the left
                    tabsPane.removeTabAt(existingTab);
                    tabsPane.insertTab(getTabTitleForMessage(msg), null, targetScroll, null, 3);
                }
            }
        }

        this.addMessageToContainers(msg, targetPanel, targetScroll);
        
        // if a case-related tab was selected when the message was received
        if(selectedTabIndex > 2) {
            for (int i = 3; i < this.tabsPane.getTabCount(); i++) {
                if (tabsPane.getTitleAt(i).equals(selectedTabCaption)) {
                    tabsPane.setSelectedIndex(i);
                    break;
                }
            }
        }

    }
    
    private String getTabTitleForMessage(InstantMessage m) {
        StringBuilder sb=new StringBuilder();
        String name=m.getCaseContext().getName();
        name=StringUtils.cutoff(name,20);
        sb.append("<html><b>").append(m.getCaseContext().getFileNumber()).append("</b><br/>").append(name).append("</html>");
        return sb.toString();
    }
    
    private String getTooltipForTab(ArchiveFileBean caseContext) {
        if(caseContext==null)
            return null;
        StringBuilder sb=new StringBuilder();
        sb.append(caseContext.getFileNumber()).append(System.lineSeparator()).append(caseContext.getName()).append(System.lineSeparator()).append(caseContext.getReason());
        return sb.toString();
    }

    private void addToMentioningTabsIfRequired(InstantMessage msg) {

        if (msg.getMentions() == null || msg.getMentions().isEmpty()) {
            return;
        }

        if (msg.hasOpenMentions() && UserSettings.getInstance().getCurrentUser().getPrincipalId().equalsIgnoreCase(msg.getSender())) {
            // there are open mentions for a message that was sent by the current user

            // check sent to self
            InstantMessageMention mention = msg.getMentionFor(UserSettings.getInstance().getCurrentUser().getPrincipalId());
            if (mention != null && !mention.isDone()) {
                this.addMessageToContainers(msg, this.pnlMessagesToMe, this.jScrollPane2);
            } else {
                this.addMessageToContainers(msg, this.pnlMessagesToOthers, this.jScrollPane4);
            }
        } else {
            // check for messages that have an open mention for the current user
            InstantMessageMention mention = msg.getMentionFor(UserSettings.getInstance().getCurrentUser().getPrincipalId());
            if (mention != null && !mention.isDone()) {
                this.addMessageToContainers(msg, this.pnlMessagesToMe, this.jScrollPane2);
            }
        }

    }

