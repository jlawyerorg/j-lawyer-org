    private void startServerPolling() {
        if (this.serverPollingTimer != null) {
            this.serverPollingTimer.stop();
        }
        this.serverPollingTimer = new javax.swing.Timer(30000, (java.awt.event.ActionEvent e) -> {
            try {
                com.jdimension.jlawyer.client.settings.ClientSettings settings = com.jdimension.jlawyer.client.settings.ClientSettings.getInstance();
                com.jdimension.jlawyer.services.JLawyerServiceLocator locator =
                    com.jdimension.jlawyer.services.JLawyerServiceLocator.getInstance(settings.getLookupProperties());
                UserSettings usettings = UserSettings.getInstance();
                AppUserBean cu = usettings.getCurrentUser();
                List<MailboxSetup> mailboxes = usettings.getMailboxes(cu.getPrincipalId());
                for (MailboxSetup ms : mailboxes) {
                    try {
                        boolean hasNew = locator.lookupEmailServiceRemote().hasNewMessages(ms.getId());
                        if (hasNew) {
                            synchronized (this.inboxFolders) {
                                FolderContainer fc = this.inboxFolders.get(ms);
                                if (fc != null) {
                                    fc.resetCaches();
                                }
                            }
                            this.treeFolders.repaint();

                            // Incremental table update if an inbox folder is currently selected
                            incrementalInboxRefresh(ms, locator);
                        }
                    } catch (Exception ex) {
                        log.error("Error polling mailbox " + ms.getEmailAddress(), ex);
                    }
                }
            } catch (Exception ex) {
                log.error("Error during server polling", ex);
            }
        });
        this.serverPollingTimer.setInitialDelay(30000);
        this.serverPollingTimer.start();
    }

    private void incrementalInboxRefresh(MailboxSetup ms, com.jdimension.jlawyer.services.JLawyerServiceLocator locator) {
        try {
            // Check if an inbox folder of this mailbox is currently selected
            if (this.treeFolders.getSelectionPath() == null) return;
            DefaultMutableTreeNode selNode = (DefaultMutableTreeNode) this.treeFolders.getSelectionPath().getLastPathComponent();
            if (!(selNode.getUserObject() instanceof FolderContainer)) return;
            FolderContainer selFc = (FolderContainer) selNode.getUserObject();
            if (!selFc.isServerBased()) return;
            if (selFc.getFolderDTO() == null || !selFc.getFolderDTO().isInbox()) return;
            if (!ms.getId().equals(selFc.getMailboxId())) return;

            // Fetch current restriction settings
            ClientSettings cs = ClientSettings.getInstance();
            String restriction = cs.getConfiguration(ClientSettings.CONF_MAIL_DOWNLOADRESTRICTION, "" + LoadFolderRestriction.RESTRICTION_50);
            int top = 50;
            java.util.Date sinceDate = null;
            boolean unreadOnly = false;
            try {
                int restr = Integer.parseInt(restriction);
                if (restr == LoadFolderRestriction.RESTRICTION_20) top = 20;
                else if (restr == LoadFolderRestriction.RESTRICTION_100) top = 100;
                else if (restr == LoadFolderRestriction.RESTRICTION_500) top = 500;
                else if (restr == LoadFolderRestriction.RESTRICTION_UNREAD) { top = 500; unreadOnly = true; }
                else if (restr == LoadFolderRestriction.RESTRICTION_LAST1W) { top = 500; sinceDate = new java.util.Date(System.currentTimeMillis() - (7L * 24 * 60 * 60 * 1000)); }
                else if (restr == LoadFolderRestriction.RESTRICTION_LAST2W) { top = 500; sinceDate = new java.util.Date(System.currentTimeMillis() - (14L * 24 * 60 * 60 * 1000)); }
                else if (restr == LoadFolderRestriction.RESTRICTION_LAST4W) { top = 500; sinceDate = new java.util.Date(System.currentTimeMillis() - (28L * 24 * 60 * 60 * 1000)); }
                else if (restr == LoadFolderRestriction.RESTRICTION_LAST2M) { top = 1000; sinceDate = new java.util.Date(System.currentTimeMillis() - (60L * 24 * 60 * 60 * 1000)); }
                else if (restr == LoadFolderRestriction.RESTRICTION_LAST6M) { top = 2000; sinceDate = new java.util.Date(System.currentTimeMillis() - (180L * 24 * 60 * 60 * 1000)); }
                else if (restr == LoadFolderRestriction.RESTRICTION_LAST1Y) { top = 5000; sinceDate = new java.util.Date(System.currentTimeMillis() - (365L * 24 * 60 * 60 * 1000)); }
            } catch (Throwable t) { }

            List<com.jdimension.jlawyer.services.MailMessageDTO> serverMessages =
                locator.lookupEmailServiceRemote().listMessages(ms.getId(), selFc.getFolderId(), top, 0, sinceDate, unreadOnly, null);

            // Build set of server messageRefs
            java.util.LinkedHashMap<String, com.jdimension.jlawyer.services.MailMessageDTO> serverMap = new java.util.LinkedHashMap<>();
            for (com.jdimension.jlawyer.services.MailMessageDTO dto : serverMessages) {
                serverMap.put(dto.getMessageRef(), dto);
            }

            // Build set of current table messageRefs
            javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) this.tblMails.getModel();
            java.util.HashSet<String> tableRefs = new java.util.HashSet<>();
            for (int i = 0; i < model.getRowCount(); i++) {
                Object obj = model.getValueAt(i, 2);
                if (obj instanceof MessageContainer) {
                    String ref = ((MessageContainer) obj).getMessageRef();
                    if (ref != null) tableRefs.add(ref);
                }
            }

            // Determine changes
            java.util.Set<String> serverRefs = serverMap.keySet();
            boolean changed = false;

            // Remove rows no longer on server
            for (int i = model.getRowCount() - 1; i >= 0; i--) {
                Object obj = model.getValueAt(i, 2);
                if (obj instanceof MessageContainer) {
                    String ref = ((MessageContainer) obj).getMessageRef();
                    if (ref != null && !serverRefs.contains(ref)) {
                        model.removeRow(i);
                        changed = true;
                    } else if (ref != null && serverRefs.contains(ref)) {
                        // Update read status if changed
                        MessageContainer mc = (MessageContainer) obj;
                        com.jdimension.jlawyer.services.MailMessageDTO serverDto = serverMap.get(ref);
                        if (serverDto != null && mc.isRead() != serverDto.isRead()) {
                            mc.getMessageDTO().setRead(serverDto.isRead());
                            model.setValueAt(mc, i, 2);
                            changed = true;
                        }
                    }
                }
            }

            // Add new rows
            java.text.SimpleDateFormat df = new java.text.SimpleDateFormat("dd.MM.yyyy, HH:mm");
            for (com.jdimension.jlawyer.services.MailMessageDTO dto : serverMessages) {
                if (!tableRefs.contains(dto.getMessageRef())) {
                    String sentString = dto.getDate() != null ? df.format(dto.getDate()) : "";
                    String from = dto.getFrom() != null ? dto.getFrom() : "";
                    String toString = "";
                    if (dto.getTo() != null && dto.getTo().length > 0) toString = dto.getTo()[0];
                    model.insertRow(0, new Object[]{sentString, from, new MessageContainer(dto, ms.getId()), toString});
                    changed = true;
                }
            }

            if (changed) {
                this.tblMails.repaint();
            }

        } catch (Exception ex) {
            log.error("Error during incremental inbox refresh", ex);
        }
    }

    private void refreshFolders(boolean showErrorDialogOnFailure) throws Exception {

        DefaultMutableTreeNode rootNode = new DefaultMutableTreeNode("E-Mail-Postfächer");

        UserSettings uset = UserSettings.getInstance();
        List<MailboxSetup> mailboxes = uset.getMailboxes(uset.getCurrentUser().getPrincipalId());

        // Limit thread pool to max 6 threads for better resource management
        int poolSize = Math.min(mailboxes.size(), 6);
        ExecutorService pool = Executors.newFixedThreadPool(poolSize);

        long initStart = System.currentTimeMillis();

        // Parallelize entire mailbox initialization (auth token, connect, folder listing)
        for (MailboxSetup ms : mailboxes) {

            Runnable mailboxInitTask = () -> {
                try {

                    {
                        // Server-based path: use unified EmailService for all mailboxes
                        try {
                            ClientSettings settings = ClientSettings.getInstance();
                            JLawyerServiceLocator locator = JLawyerServiceLocator.getInstance(settings.getLookupProperties());
                            // Token refresh is now handled server-side via Client Credentials Flow

                            List<com.jdimension.jlawyer.services.MailFolderDTO> folders = locator.lookupEmailServiceRemote().listFolders(ms.getId());

                            FolderContainer inboxContainer = null;
                            FolderContainer trashContainer = null;
                            FolderContainer sentContainer = null;

                            for (com.jdimension.jlawyer.services.MailFolderDTO f : folders) {
                                if (f.isInbox()) {
                                    inboxContainer = new FolderContainer(f, ms.getId());
                                } else if (f.isTrash()) {
                                    trashContainer = new FolderContainer(f, ms.getId());
                                } else if (f.isSent()) {
                                    sentContainer = new FolderContainer(f, ms.getId());
                                }
                            }

                            // Fallback: use first folder as inbox if none matched
                            if (inboxContainer == null && !folders.isEmpty()) {
                                inboxContainer = new FolderContainer(folders.get(0), ms.getId());
                            }
                            if (trashContainer == null) {
                                // Create a dummy trash container
                                com.jdimension.jlawyer.services.MailFolderDTO trashDto = new com.jdimension.jlawyer.services.MailFolderDTO();
                                trashDto.setFolderId("deleteditems");
                                trashDto.setDisplayName("Deleted Items");
                                trashContainer = new FolderContainer(trashDto, ms.getId());
                            }
                            if (sentContainer == null) {
                                com.jdimension.jlawyer.services.MailFolderDTO sentDto = new com.jdimension.jlawyer.services.MailFolderDTO();
                                sentDto.setFolderId("sentitems");
                                sentDto.setDisplayName("Sent Items");
                                sentContainer = new FolderContainer(sentDto, ms.getId());
                            }

                            synchronized (this.inboxFolders) { this.inboxFolders.put(ms, inboxContainer); }
                            synchronized (this.trashFolders) { this.trashFolders.put(ms, trashContainer); }
                            synchronized (this.sentFolders) { this.sentFolders.put(ms, sentContainer); }

                            // No IDLE thread needed for server-based mailboxes (server polls)

                            DefaultMutableTreeNode mailboxRootNode = new DefaultMutableTreeNode(ms.getEmailAddress());
                            synchronized (rootNode) {
                                rootNode.add(mailboxRootNode);
                            }

                            // Build folder tree from server DTOs with parent-child hierarchy
                            HashMap<String, DefaultMutableTreeNode> nodeMap = new HashMap<>();

                            // Build a lookup map for path construction
                            HashMap<String, com.jdimension.jlawyer.services.MailFolderDTO> dtoMap = new HashMap<>();
                            for (com.jdimension.jlawyer.services.MailFolderDTO f : folders) {
                                dtoMap.put(f.getFolderId(), f);
                            }

                            // Collect hidden folder IDs (including children of hidden folders)
                            java.util.HashSet<String> hiddenFolderIds = new java.util.HashSet<>();
                            for (com.jdimension.jlawyer.services.MailFolderDTO f : folders) {
                                String fullFolderPath = buildDtoFolderPath(f, dtoMap);
                                if (MailboxSettings.getInstance().isFolderHidden(ms, fullFolderPath)) {
                                    hiddenFolderIds.add(f.getFolderId());
                                }
                            }
                            // Also hide children of hidden folders
                            boolean changed = true;
                            while (changed) {
                                changed = false;
                                for (com.jdimension.jlawyer.services.MailFolderDTO f : folders) {
                                    if (!hiddenFolderIds.contains(f.getFolderId()) && f.getParentFolderId() != null && hiddenFolderIds.contains(f.getParentFolderId())) {
                                        hiddenFolderIds.add(f.getFolderId());
                                        changed = true;
                                    }
                                }
                            }

                            // First pass: create nodes for non-hidden folders
                            for (com.jdimension.jlawyer.services.MailFolderDTO f : folders) {
                                if (hiddenFolderIds.contains(f.getFolderId())) {
                                    continue;
                                }
                                FolderContainer fc = new FolderContainer(f, ms.getId());
                                DefaultMutableTreeNode node = new DefaultMutableTreeNode(fc);
                                nodeMap.put(f.getFolderId(), node);
                                if (f.isInbox()) {
                                    synchronized (this.inboxFolderNodes) {
                                        this.inboxFolderNodes.put(ms, node);
                                    }
                                }
                            }

                            // Second pass: build hierarchy
                            for (com.jdimension.jlawyer.services.MailFolderDTO f : folders) {
                                if (!nodeMap.containsKey(f.getFolderId())) continue; // was hidden
                                DefaultMutableTreeNode node = nodeMap.get(f.getFolderId());
                                if (f.getParentFolderId() != null && nodeMap.containsKey(f.getParentFolderId())) {
                                    nodeMap.get(f.getParentFolderId()).add(node);
                                } else {
                                    mailboxRootNode.add(node);
                                }
                            }

                            // Third pass: sort children of each node alphabetically
                            sortTreeNodeChildren(mailboxRootNode);

                        } catch (Exception ex) {
                            log.error("Error initializing server-based mailbox " + ms.getEmailAddress(), ex);
                            if (showErrorDialogOnFailure) {
                                javax.swing.SwingUtilities.invokeLater(() -> {
                                    JOptionPane.showMessageDialog(this, "Fehler beim Verbinden mit " + ms.getEmailAddress() + ": " + ex.getMessage(), com.jdimension.jlawyer.client.utils.DesktopUtils.POPUP_TITLE_ERROR, JOptionPane.ERROR_MESSAGE);
                                });
                            }
                        }
                        return; // server-based path handles all mailboxes
                    }

                } catch (Exception ex) {
                    log.error("Unable to load mailbox " + ms.getEmailAddress(), ex);
                }
            };

            pool.execute(mailboxInitTask);
        }

        pool.shutdown();
        try {
            pool.awaitTermination(60, TimeUnit.SECONDS);
        } catch (Throwable t) {
            log.error("error loading mailboxes", t);
        }
        log.info("Parallel mailbox initialization took " + (System.currentTimeMillis() - initStart) + "ms");

        // Sort mailboxes alphabetically by email address
        sortTreeNodeChildren(rootNode);

        Runnable r = () -> {
            try {
                DefaultTreeModel tm = new DefaultTreeModel(rootNode);
                treeFolders.setModel(tm);
                this.restoreExpansionState();
                treeFolders.setCellRenderer(renderer);

            } catch (Throwable t) {
                log.error("Could not load folder");
            }
        };

        SwingUtilities.invokeLater(r);

    }

    private void restoreExpansionState() {
        ClientSettings cs = ClientSettings.getInstance();
        String[] values = cs.getConfigurationArray(ClientSettings.CONF_MAIL_COLLAPSEDFOLDERS, new String[0]);
        ArrayList<String> valueList = new java.util.ArrayList<>(Arrays.asList(values));

        // Repeat expansion until no new rows appear (needed for nested nodes)
        int previousRowCount = -1;
        while (previousRowCount != this.treeFolders.getRowCount()) {
            previousRowCount = this.treeFolders.getRowCount();
            for (int i = 0; i < this.treeFolders.getRowCount(); i++) {
                TreePath tp = this.treeFolders.getPathForRow(i);
                String fullName = this.getFullPath(tp);

                if (!(valueList.contains(fullName))) {
                    this.treeFolders.expandRow(i);
                }
            }
        }

    }

