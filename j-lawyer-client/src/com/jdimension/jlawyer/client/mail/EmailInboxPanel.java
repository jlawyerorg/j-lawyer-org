    private MailboxSetup getSelectedMailbox() {
        if (this.treeFolders.getSelectionPath() != null) {
            DefaultMutableTreeNode selectedNode = (DefaultMutableTreeNode) this.treeFolders.getSelectionPath().getLastPathComponent();
            while (selectedNode.getUserObject() instanceof FolderContainer) {
                selectedNode = (DefaultMutableTreeNode) selectedNode.getParent();
            }
            if (selectedNode.toString().contains("@")) {
                String mailAddress = selectedNode.toString();
                UserSettings uset = UserSettings.getInstance();
                List<MailboxSetup> mailboxes = uset.getMailboxes(uset.getCurrentUser().getPrincipalId());
                for (MailboxSetup ms : mailboxes) {
                    if (ms.getEmailAddress().equalsIgnoreCase(mailAddress)) {
                        return ms;
                    }
                }
            }
        }
        return null;
    }

    @Override
    public boolean saveToCaseCallback(String caseId, boolean withAttachments, boolean separateAttachments, boolean attachmentsOnly) {

        if (this.tblMails.getSelectedRowCount() == 0) {
            return false;
        }

        StringBuilder searchContext = new StringBuilder();
        for (int row : this.tblMails.getSelectedRows()) {
            MessageContainer msgC = (MessageContainer) this.tblMails.getValueAt(row, 2);
            try {
                if (msgC.isServerBased()) {
                    searchContext.append(msgC.getMessageDTO().getSubject()).append(" ");
                } else {
                    Message m = msgC.getMessage();
                    searchContext.append(m.getSubject()).append(" ");
                }
            } catch (Exception ex) {
                log.warn("unable to retrieve message subject", ex);
            }
        }

        CaseFolder rootFolder = null;
        CaseFolder targetFolder = null;
        ArchiveFileBean targetCase = null;
        if (caseId == null) {
            SearchAndAssignDialog dlg = new SearchAndAssignDialog(EditorsRegistry.getInstance().getMainWindow(), true, searchContext.toString(), null);
            dlg.setVisible(true);
            targetCase = dlg.getCaseSelection();
            targetFolder = dlg.getFolderSelection();
            rootFolder = dlg.getRootFolder();
            dlg.dispose();

        } else {
            SearchAndAssignDialog dlg = new SearchAndAssignDialog(EditorsRegistry.getInstance().getMainWindow(), true, searchContext.toString(), caseId);
            dlg.setVisible(true);
            dlg.dispose();
            targetFolder = dlg.getFolderSelection();
            targetCase = dlg.getCaseSelection();
            rootFolder = dlg.getRootFolder();

        }

        // user hit cancel
        if (targetCase == null) {
            return false;
        }

        BulkSaveDialog bulkSaveDlg = new BulkSaveDialog(BulkSaveDialog.TYPE_MAIL, EditorsRegistry.getInstance().getMainWindow(), true);
        bulkSaveDlg.setCaseFolder(rootFolder, targetFolder);
        bulkSaveDlg.setSelectedCase(targetCase);
        MailboxSetup ms = this.getSelectedMailbox();

        Folder f = null;
        try {
            for (int row : this.tblMails.getSelectedRows()) {

                MessageContainer msgC = (MessageContainer) this.tblMails.getValueAt(row, 2);

                // Server-based save-to-case
                if (msgC.isServerBased()) {
                    try {
                        ClientSettings settings = ClientSettings.getInstance();
                        com.jdimension.jlawyer.services.JLawyerServiceLocator locator = com.jdimension.jlawyer.services.JLawyerServiceLocator.getInstance(settings.getLookupProperties());
                        com.jdimension.jlawyer.services.MailMessageDTO fullMsg = locator.lookupEmailServiceRemote().getMessage(msgC.getMailboxId(), msgC.getMessageRef());
                        java.util.List<com.jdimension.jlawyer.services.MailAttachmentDTO> atts = locator.lookupEmailServiceRemote().getAttachments(msgC.getMailboxId(), msgC.getMessageRef());

                        // Get EML via server (single source of truth for EML generation)
                        byte[] data = locator.lookupEmailServiceRemote().getMessageAsEml(msgC.getMailboxId(), msgC.getMessageRef());
                        Date sentDate = fullMsg.getDate();

                        if (attachmentsOnly && atts != null) {
                            for (com.jdimension.jlawyer.services.MailAttachmentDTO att : atts) {
                                if (!att.isInline() && att.getContent() != null) {
                                    BulkSaveEntry bulkEntry = new BulkSaveEntry();
                                    bulkEntry.setDocumentDate(sentDate);
                                    bulkEntry.setDocumentBytes(att.getContent());
                                    bulkEntry.setDocumentFilename(FileUtils.sanitizeFileName(att.getName()));
                                    bulkSaveDlg.addEntry(bulkEntry);
                                }
                            }
                        }
                        if (!attachmentsOnly) {
                            BulkSaveEntry bulkEntry = new BulkSaveEntry();
                            bulkEntry.setDocumentDate(sentDate);
                            String docName = fullMsg.getSubject() != null ? fullMsg.getSubject() : "Email";
                            docName = FileUtils.sanitizeFileName(docName);
                            if (!docName.toLowerCase().endsWith(".eml")) docName += ".eml";
                            bulkEntry.setDocumentFilename(docName);
                            bulkEntry.setDocumentBytes(data);
                            bulkSaveDlg.addEntry(bulkEntry);

                            if (separateAttachments && withAttachments && atts != null) {
                                for (com.jdimension.jlawyer.services.MailAttachmentDTO att : atts) {
                                    if (!att.isInline() && att.getContent() != null) {
                                        BulkSaveEntry attEntry = new BulkSaveEntry();
                                        attEntry.setDocumentDate(sentDate);
                                        attEntry.setDocumentBytes(att.getContent());
                                        attEntry.setDocumentFilename(FileUtils.sanitizeFileName(att.getName()));
                                        bulkSaveDlg.addEntry(attEntry);
                                    }
                                }
                            }
                        }
                    } catch (Exception ex) {
                        log.error("Error saving server-based message to case", ex);
                    }
                    continue;
                }

                // Legacy IMAP path
                Message m = msgC.getMessage();

                if (ms == null) {
                    ms = EmailUtils.getMailboxSetup(m);
                }

                // may happen if neither the FROM nor the recipients of the mail contain an address that has a mailbox setup configuration
                if (ms == null) {
                    log.warn("Mailbox setup is unknown when saving message - falling back to first available");
                    UserSettings uset = UserSettings.getInstance();
                    List<MailboxSetup> mailboxes = uset.getMailboxes(uset.getCurrentUser().getPrincipalId());
                    if (!mailboxes.isEmpty()) {
                        ms = mailboxes.get(0);
                    }
                }

                Properties props = System.getProperties();
                props.setProperty("mail.store.protocol", ms.getEmailInType());
                if (ms.isEmailInSsl()) {
                    props.setProperty("mail." + ms.getEmailInType() + ".ssl.enable", "true");
                }

                Date sentDate = m.getSentDate();

                f = m.getFolder();
                if (f != null) {
                    if (!f.isOpen()) {
                        System.out.println("open 18");
                        f.open(Folder.READ_WRITE);
                    }
                }

                byte[] data = null;

                if (withAttachments) {
                    ByteArrayOutputStream bOut = new ByteArrayOutputStream();
                    m.writeTo(bOut);
                    bOut.close();
                    data = bOut.toByteArray();
                } else {
                    ByteArrayOutputStream bos = new ByteArrayOutputStream();
                    m.writeTo(bos);
                    bos.close();
                    SharedByteArrayInputStream bis
                            = new SharedByteArrayInputStream(bos.toByteArray());
                    Session session = Session.getDefaultInstance(props, null);
                    MimeMessage copiedMsg = new MimeMessage(session, bis);
                    bis.close();
                    if (copiedMsg.isMimeType("multipart/*")) {
                        MimeMultipart mmp = (MimeMultipart) copiedMsg.getContent();
                        for (int i = 0; i < mmp.getCount(); i++) {
                            BodyPart part = mmp.getBodyPart(i);
                            if (!StringUtils.isEmpty(part.getFileName())) {
                                mmp.removeBodyPart(i);

                                i--;
                            } // +++++ end of if (hasToBeRemoved(part))
                        } // +++++ end of for (int i = 0; i < mmp.getCount(); i++)
                        copiedMsg.setContent(mmp);
                        copiedMsg.saveChanges();
                    }
                    ByteArrayOutputStream bOut = new ByteArrayOutputStream();
                    copiedMsg.writeTo(bOut);
                    bOut.close();
                    data = bOut.toByteArray();
                }

                if (attachmentsOnly) {
                    ArrayList<String> attachmentNames = EmailUtils.getAttachmentNames(m.getContent());
                    for (String attachmentName : attachmentNames) {

                        BulkSaveEntry bulkEntry = new BulkSaveEntry();
                        bulkEntry.setDocumentDate(sentDate);

                        byte[] attachmentData = EmailUtils.getAttachmentBytes(attachmentName, msgC);

                        bulkEntry.setDocumentBytes(attachmentData);

                        String newName = attachmentName;
                        if (newName == null) {
                            newName = "";
                        }
                        newName = FileUtils.sanitizeFileName(newName);
                        bulkEntry.setDocumentFilename(newName);
                        bulkSaveDlg.addEntry(bulkEntry);
                    }
                }

                if (!attachmentsOnly) {

                    String newName = m.getSubject();
                    if (newName == null) {
                        newName = "";
                    }
                    newName = newName + ".eml";
                    newName = FileUtils.sanitizeFileName(newName);

                    BulkSaveEntry bulkEntry = new BulkSaveEntry();
                    bulkEntry.setDocumentDate(sentDate);
                    bulkEntry.setDocumentBytes(data);
                    bulkEntry.setDocumentFilename(newName);
                    bulkSaveDlg.addEntry(bulkEntry);

                }

                if (separateAttachments && !attachmentsOnly) {
                    ArrayList<String> attachmentNames = EmailUtils.getAttachmentNames(m.getContent());
                    for (String attachmentName : attachmentNames) {

                        BulkSaveEntry bulkEntry = new BulkSaveEntry();
                        bulkEntry.setDocumentDate(sentDate);

                        byte[] attachmentData = EmailUtils.getAttachmentBytes(attachmentName, msgC);
                        String newName = attachmentName;
                        if (newName == null) {
                            newName = "";
                        }
                        newName = FileUtils.sanitizeFileName(newName);
                        String formerName = newName;
                        newName = FileUtils.preserveExtension(formerName, newName);
                        newName = FileUtils.sanitizeFileName(newName);

                        if (newName.trim().length() == 0) {
                            newName = "Anhang";
                        }

                        bulkEntry.setDocumentBytes(attachmentData);
                        bulkEntry.setDocumentFilename(newName);
                        bulkSaveDlg.addEntry(bulkEntry);

                    }

                }

            }

            if (f != null) {
                this.resetCacheForSelectedFolder(f);
            }
            if (!EmailUtils.isInbox(f)) {
                EmailUtils.closeIfIMAP(f);
            }

        } catch (Exception ex) {
            log.error(ex);
            ThreadUtils.showErrorDialog(EditorsRegistry.getInstance().getMainWindow(), "Fehler beim Speichern der E-Mail: " + ex.getMessage(), com.jdimension.jlawyer.client.utils.DesktopUtils.POPUP_TITLE_ERROR);
        }

        FrameUtils.centerDialog(bulkSaveDlg, EditorsRegistry.getInstance().getMainWindow());
        bulkSaveDlg.setVisible(true);

        if (!bulkSaveDlg.isFailedOrCancelled()) {
            String temp = ClientSettings.getInstance().getConfiguration(ClientSettings.CONF_MAILS_DELETEENABLED, "false");
            if ("true".equalsIgnoreCase(temp)) {
                this.cmdDeleteActionPerformed(null);
            } else if (caseId == null && targetCase != null) {
                SaveToCasePanel ep = new SaveToCasePanel(this.getClass().getName());
                ep.setBackground(ep.getBackground().brighter());
                CaseForContactEntry lce = new CaseForContactEntry();
                lce.setFileNumber(targetCase.getFileNumber());
                lce.setId(targetCase.getId());
                lce.setRole("");
                lce.setName(targetCase.getName());
                lce.setReason(StringUtils.nonEmpty(targetCase.getReason()));
                lce.setArchived(targetCase.isArchived());
                ep.setEntry(lce, this);

                this.pnlActionsChild.add(ep, 0);
                GridLayout layout = (GridLayout) this.pnlActionsChild.getLayout();
                layout.setRows(layout.getRows() + 1);
                this.pnlActionsChild.revalidate();
                this.pnlActionsChild.repaint();
            }
            return true;
        } else {
            return false;
        }
    }

