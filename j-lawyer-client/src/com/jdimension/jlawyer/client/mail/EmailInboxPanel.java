    // returns the full path including the mailboxes mail adress as root
    private void sortTreeNodeChildren(DefaultMutableTreeNode parent) {
        if (parent.getChildCount() <= 1) return;

        // Collect children
        java.util.List<DefaultMutableTreeNode> children = new java.util.ArrayList<>();
        for (int i = 0; i < parent.getChildCount(); i++) {
            children.add((DefaultMutableTreeNode) parent.getChildAt(i));
        }

        // Sort case-insensitive by display name
        children.sort((a, b) -> {
            String nameA = a.getUserObject() != null ? a.getUserObject().toString() : "";
            String nameB = b.getUserObject() != null ? b.getUserObject().toString() : "";
            return nameA.compareToIgnoreCase(nameB);
        });

        // Re-add in sorted order
        parent.removeAllChildren();
        for (DefaultMutableTreeNode child : children) {
            parent.add(child);
            // Recursively sort children of each child
            sortTreeNodeChildren(child);
        }
    }

    private String buildDtoFolderPath(com.jdimension.jlawyer.services.MailFolderDTO dto, HashMap<String, com.jdimension.jlawyer.services.MailFolderDTO> dtoMap) {
        java.util.LinkedList<String> parts = new java.util.LinkedList<>();
        com.jdimension.jlawyer.services.MailFolderDTO current = dto;
        while (current != null) {
            parts.addFirst(current.getDisplayName());
            current = (current.getParentFolderId() != null) ? dtoMap.get(current.getParentFolderId()) : null;
        }
        return String.join(File.separator, parts);
    }

    private String getFullPathImpl(TreePath tp, int startIndex) {
        StringBuilder sb = new StringBuilder();

        Object[] paths = tp.getPath();
        // ignore the root, start at index 1
        for (int i = startIndex; i < paths.length; i++) {
            String name = paths[i].toString();
            if (paths[i] instanceof DefaultMutableTreeNode) {
                if (((DefaultMutableTreeNode) paths[i]).getUserObject() instanceof FolderContainer) {
                    FolderContainer pathFc = (FolderContainer) ((DefaultMutableTreeNode) paths[i]).getUserObject();
                    if (pathFc.isServerBased()) {
                        name = pathFc.getFolderDTO().getDisplayName();
                    } else {
                        name = pathFc.getFolder().getName();
                    }
                }

            }
            sb.append(name);
            if (i + 1 < paths.length) {
                sb.append(File.separator);
            }
        }
        return sb.toString();
    }

    private void displayMessage() {

        if (this.tblMails.getSelectedRow() < 0) {
            // none selected
            this.mailContentUI.clear();
            this.pnlActionsChild.removeAll();
            return;
        } else if (this.tblMails.getSelectedRows().length > 1) {
            // multiple selected
            this.mailContentUI.clear();
            this.pnlActionsChild.removeAll();
            SaveToCasePanel sp = new SaveToCasePanel(this.getClass().getName());
            CaseForContactEntry cce = new CaseForContactEntry();
            cce.setFileNumber(null);
            cce.setId(null);
            cce.setRole("");
            cce.setName("Akte suchen und zuordnen");
            cce.setReason(StringUtils.nonEmpty(null));
            cce.setArchived(false);
            sp.setEntry(cce, this);
            sp.setBackground(sp.getBackground().brighter());
            this.pnlActionsChild.add(sp);
            this.pnlActions.revalidate();
            return;
        }

        int selectionIndex = this.tblMails.getSelectedRow();

        MessageContainer msgC = (MessageContainer) this.tblMails.getValueAt(selectionIndex, 2);

        this.pnlActionsChild.removeAll();
        this.pnlActions.revalidate();
        try {
            DefaultMutableTreeNode selNode = (DefaultMutableTreeNode) this.treeFolders.getSelectionPath().getLastPathComponent();
            MailboxSetup ms = this.getMailboxSetup(selNode);
            boolean messageLoaded = this.mailContentUI.setMessage(msgC, ms);
            if (!messageLoaded) {
                if (this.treeFolders.getSelectionPath() != null) {
                    DefaultMutableTreeNode selectedNode = (DefaultMutableTreeNode) this.treeFolders.getSelectionPath().getLastPathComponent();
                    if (selectedNode.getUserObject() instanceof FolderContainer) {
                        ((FolderContainer) selectedNode.getUserObject()).resetCaches();
                    }
                    this.cmbDownloadMailsActionPerformed(null);
                }
            }

            final long currentActionLoadId = ++this.actionPanelLoadId;

            if (msgC.isServerBased()) {
                // Server-based: run action panel population in background thread
                // Body is loaded asynchronously by MailContentUI, so we need to
                // wait for it or load it ourselves to avoid race conditions
                new Thread(() -> {
                    populateActionPanel(msgC, currentActionLoadId, selectionIndex);
                }).start();
            } else {
                // Legacy path: local data, body is available synchronously
                populateActionPanel(msgC, currentActionLoadId, selectionIndex);
            }

        } catch (Throwable t) {
            log.error("Error displaying message", t);
        }
    }

    private void populateActionPanel(MessageContainer msgC, long loadId, int selectionIndex) {
        ArrayList<Component> actionPanelEntries = new ArrayList<>();
        ClientSettings settings = ClientSettings.getInstance();
        try {
            JLawyerServiceLocator locator = JLawyerServiceLocator.getInstance(settings.getLookupProperties());
            AddressServiceRemote ads = locator.lookupAddressServiceRemote();
            ArchiveFileServiceRemote afs = locator.lookupArchiveFileServiceRemote();

            // For server-based messages, get body from DTO (may need to wait for it to load)
            String body;
            if (msgC.isServerBased()) {
                MailMessageDTO dto = msgC.getMessageDTO();
                // Wait for body to become available (loaded by MailContentUI's background thread)
                int waitCount = 0;
                while (dto.getBody() == null && waitCount < 100) {
                    Thread.sleep(50);
                    waitCount++;
                    dto = msgC.getMessageDTO();
                    if (loadId != this.actionPanelLoadId) return;
                }
                body = dto.getBody() != null ? dto.getBody() : "";
            } else {
                body = this.mailContentUI.getBody();
            }

            if (loadId != this.actionPanelLoadId) return;

            ArrayList<String> phoneNumbers = new ArrayList<>();
            try {
                Collection<Keyword> keywords = afs.extractKeywordsFromText(body);
                for (Keyword w : keywords) {
                    if (w.getType() == Keyword.TYPE_PHONENR) {
                        phoneNumbers.add(w.getValue());
                    }
                }
            } catch (Throwable t) {
                log.error("Could not extract keywords from email", t);
            }

            if (loadId != this.actionPanelLoadId) return;

            // find any potential cases by looking for file numbers in subject and body
            if ((System.currentTimeMillis() - this.lastFileNumbersUpdate) > 15000l) {
                this.allFileNumbers = afs.getAllArchiveFileNumbers(true);
                this.lastFileNumbersUpdate = System.currentTimeMillis();
            }
            ArrayList<ArchiveFileBean> subjectBodyCases = new ArrayList<>();

            String subject = msgC.isServerBased() ? (msgC.getMessageDTO().getSubject() != null ? msgC.getMessageDTO().getSubject().toLowerCase() : "") : msgC.getMessage().getSubject().toLowerCase();
            String bodyLower = body.toLowerCase();

            // Deduplicate: collect matching file numbers first, then do one lookup per unique match
            Set<String> matchedFileNumbers = new LinkedHashSet<>();
            for (String fn : allFileNumbers) {
                String fnLower = fn.toLowerCase();
                if (subject.contains(fnLower) || bodyLower.contains(fnLower)) {
                    matchedFileNumbers.add(fn);
                }
            }
            for (String fn : matchedFileNumbers) {
                ArchiveFileBean a = afs.getArchiveFileByFileNumber(fn);
                if (a != null) {
                    subjectBodyCases.add(a);
                }
            }

            if (loadId != this.actionPanelLoadId) return;

            // find by foreign file numbers (deduplicated)
            if ((System.currentTimeMillis() - this.lastForeignFileNumbersUpdate) > 15000l) {
                this.allForeignFileNumbers = afs.getAllReferencedFileNumbers(5, true);
                this.lastForeignFileNumbersUpdate = System.currentTimeMillis();
            }
            Set<String> matchedForeignFileNumbers = new LinkedHashSet<>();
            for (String fn : allForeignFileNumbers) {
                String fnLower = fn.toLowerCase();
                if (!StringUtils.isEmpty(fnLower)) {
                    if (subject.contains(fnLower) || bodyLower.contains(fnLower)) {
                        matchedForeignFileNumbers.add(fn);
                    }
                }
            }
            for (String fn : matchedForeignFileNumbers) {
                List<ArchiveFileAddressesBean> a = afs.getArchiveFileAddressesByReference(fn);
                if (a != null) {
                    for (ArchiveFileAddressesBean aab : a) {
                        subjectBodyCases.add(aab.getArchiveFileKey());
                    }
                }
                if (subjectBodyCases.size() > 20)
                    break;
            }

            if (loadId != this.actionPanelLoadId) return;

            AddressBean[] relevantAddresses = null;
            String senderName = "";
            String senderAddress = "";
            if (msgC.isServerBased()) {
                String fromStr = msgC.getMessageDTO().getFrom();
                if (fromStr != null) {
                    // Extract email from "Name <email>" format
                    String email = fromStr;
                    if (fromStr.contains("<") && fromStr.contains(">")) {
                        email = fromStr.substring(fromStr.indexOf("<") + 1, fromStr.indexOf(">"));
                        senderName = fromStr.substring(0, fromStr.indexOf("<")).trim();
                    }
                    senderAddress = email;
                    relevantAddresses = ads.searchSimple(email);
                }
            } else {
                Address[] senders = msgC.getMessage().getFrom();
                if (senders != null && senders.length > 0) {
                    Address sender = senders[0];
                    if (sender instanceof javax.mail.internet.InternetAddress) {
                        relevantAddresses = ads.searchSimple(((javax.mail.internet.InternetAddress) sender).getAddress());
                        senderName = ((javax.mail.internet.InternetAddress) sender).getPersonal();
                        if (senderName == null) senderName = "";
                        senderAddress = ((javax.mail.internet.InternetAddress) sender).getAddress();
                        if (senderAddress == null) senderAddress = "";
                    }
                }
            }

            if (loadId != this.actionPanelLoadId) return;

            String subjectForCase = msgC.isServerBased() ? (msgC.getMessageDTO().getSubject() != null ? msgC.getMessageDTO().getSubject() : "") : (msgC.getMessage() != null ? msgC.getMessage().getSubject() : "");
            CreateNewCasePanel cncp = new CreateNewCasePanel(this.getClass().getName(), this, relevantAddresses, subjectForCase, this.mailContentUI, senderName, senderAddress);
            actionPanelEntries.add(cncp);

            ArrayList<ArchiveFileBean> addressRelatedCases = new ArrayList<>();
            if (relevantAddresses != null && relevantAddresses.length > 0) {
                AddressBean[] hits = relevantAddresses;

                if (hits.length == 0) {
                    CreateNewAddressPanel nap = new CreateNewAddressPanel(this.getClass().getName());
                    nap.setBackground(new Color(153, 204, 255));
                    nap.setEmail(senderAddress);
                    nap.setSenderName(senderName);
                    actionPanelEntries.add(nap);
                }

                // empt case reference - will trigger a search
                SaveToCasePanel sp = new SaveToCasePanel(this.getClass().getName());
                sp.setBackground(sp.getBackground().brighter());
                CaseForContactEntry cce = new CaseForContactEntry();
                cce.setFileNumber(null);
                cce.setId(null);
                cce.setRole("");
                cce.setName("Akte suchen und zuordnen");
                cce.setReason(StringUtils.nonEmpty(null));
                cce.setArchived(false);
                sp.setEntry(cce, this);
                actionPanelEntries.add(sp);

                if (!phoneNumbers.isEmpty()) {
                    ExtractedPhoneNumbersPanel pnp = new ExtractedPhoneNumbersPanel();
                    pnp.setPhoneNumbers(phoneNumbers);
                    actionPanelEntries.add(pnp);
                }

                ArrayList<String> addedAddressIds = new ArrayList<>();
                for (AddressBean h : hits) {
                    if (!addedAddressIds.contains(h.getId())) {
                        NavigateToAddressPanel ap = new NavigateToAddressPanel(this.getClass().getName());
                        ap.setAddress(h);
                        ap.setBackground(new Color(153, 204, 255));
                        actionPanelEntries.add(ap);
                        addedAddressIds.add(h.getId());
                    }
                }

                // find cases that might be relevant for the sender of the mail
                {
                    ArrayList<String> addedFileNumbers = new ArrayList<>();
                    Set<String> subjectBodyFileNumbers = new HashSet<>();
                    for (ArchiveFileBean af : subjectBodyCases) {
                        subjectBodyFileNumbers.add(af.getFileNumber());
                    }

                    for (AddressBean h : hits) {
                        Collection caseCol = afs.getArchiveFileAddressesForAddress(h.getId());

                        for (Object o : caseCol) {
                            ArchiveFileAddressesBean afab = (ArchiveFileAddressesBean) o;
                            ArchiveFileBean a = afab.getArchiveFileKey();
                            if (!addedFileNumbers.contains(a.getFileNumber())
                                    && !subjectBodyFileNumbers.contains(a.getFileNumber())) {
                                addedFileNumbers.add(afab.getArchiveFileKey().getFileNumber());
                                addressRelatedCases.add(a);
                            }
                        }

                        Collections.sort(addressRelatedCases, (Object o1, Object o2) -> {
                            ArchiveFileBean aFile1 = (ArchiveFileBean) o1;
                            ArchiveFileBean aFile2 = (ArchiveFileBean) o2;

                            boolean archived1 = aFile1.isArchived();
                            boolean archived2 = aFile2.isArchived();

                            if (archived1 && archived2) {
                                // both archived
                                // sort by changed date
                                return new FileNumberComparatorArchiveFileBean().reversed().compare(aFile1, aFile2);
                            } else if (archived1) {
                                // only 1 is archived
                                return 1;
                            } else if (archived2) {
                                // only 2 is archived
                                return -1;
                            } else {
                                // both are non-archived
                                // sort by changed date
                                return new FileNumberComparatorArchiveFileBean().reversed().compare(aFile1, aFile2);
                            }

                        });

                    }
                }
            } else if (senderAddress != null && !senderAddress.isEmpty()) {
                // No matching addresses found - offer to create a new contact
                CreateNewAddressPanel nap = new CreateNewAddressPanel(this.getClass().getName());
                nap.setBackground(new Color(153, 204, 255));
                nap.setEmail(senderAddress);
                nap.setSenderName(senderName);
                actionPanelEntries.add(nap);

                // empt case reference - will trigger a search
                SaveToCasePanel sp = new SaveToCasePanel(this.getClass().getName());
                sp.setBackground(sp.getBackground().brighter());
                CaseForContactEntry cce = new CaseForContactEntry();
                cce.setFileNumber(null);
                cce.setId(null);
                cce.setRole("");
                cce.setName("Akte suchen und zuordnen");
                cce.setReason(StringUtils.nonEmpty(null));
                cce.setArchived(false);
                sp.setEntry(cce, this);
                actionPanelEntries.add(sp);

                if (!phoneNumbers.isEmpty()) {
                    ExtractedPhoneNumbersPanel pnp = new ExtractedPhoneNumbersPanel();
                    pnp.setPhoneNumbers(phoneNumbers);
                    actionPanelEntries.add(pnp);
                }
            }

            // die spezifischeren Ergebnisse sollen oben stehen
            List<ArchiveFileBean> orderedCases = new ArrayList<>();
            if (subjectBodyCases.size() >= addressRelatedCases.size()) {
                orderedCases.addAll(addressRelatedCases);
                orderedCases.addAll(subjectBodyCases);
            } else {
                orderedCases.addAll(subjectBodyCases);
                orderedCases.addAll(addressRelatedCases);
            }
            int i = 0;
            for (Object c : orderedCases) {
                ArchiveFileBean aFile = (ArchiveFileBean) c;
                SaveToCasePanel ep = new SaveToCasePanel(this.getClass().getName());
                if (i % 2 == 0) {
                    ep.setBackground(ep.getBackground().darker());
                }
                ep.setBackground(ep.getBackground().brighter());
                CaseForContactEntry lce = new CaseForContactEntry();
                lce.setFileNumber(aFile.getFileNumber());
                lce.setId(aFile.getId());
                lce.setRole("");
                lce.setName(aFile.getName());
                lce.setReason(StringUtils.nonEmpty(aFile.getReason()));
                lce.setArchived(aFile.isArchived());
                ep.setEntry(lce, this);

                actionPanelEntries.add(ep);
                i++;
            }

            if (loadId != this.actionPanelLoadId) return;

            // Update UI on EDT
            final ArrayList<Component> finalEntries = actionPanelEntries;
            SwingUtilities.invokeLater(() -> {
                if (loadId != this.actionPanelLoadId) return;

                this.pnlActionsChild.removeAll();
                this.pnlActionsChild.setLayout(new GridLayout(finalEntries.size(), 1));
                for (Component o : finalEntries) {
                    this.pnlActionsChild.add(o);
                }

                this.jSplitPane3.setDividerLocation(0.8f);
                this.pnlActionsChild.revalidate();
                this.pnlActionsChild.repaint();

                // Update table row to reflect read status
                if (selectionIndex >= 0 && selectionIndex < tblMails.getRowCount()) {
                    tblMails.setValueAt(msgC, selectionIndex, 2);
                    for (int j = 1; j < tblMails.getColumnCount(); j++) {
                        tblMails.setValueAt(tblMails.getValueAt(selectionIndex, j), selectionIndex, j);
                    }
                }
            });

        } catch (Throwable t) {
            log.error("Error populating action panel", t);
        }
    }

