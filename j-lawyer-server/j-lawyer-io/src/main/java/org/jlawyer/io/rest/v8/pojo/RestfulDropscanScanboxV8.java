package org.jlawyer.io.rest.v8.pojo;

/**
 * A Dropscan scanbox as returned by the scanbox-discovery endpoint
 * ({@code POST /v8/dropscan/discover-scanboxes}): its numeric id and display number.
 *
 * @author jens
 */
public class RestfulDropscanScanboxV8 {

    private int id;
    private String number;

    public RestfulDropscanScanboxV8() {
    }

    public RestfulDropscanScanboxV8(int id, String number) {
        this.id = id;
        this.number = number;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }
}
