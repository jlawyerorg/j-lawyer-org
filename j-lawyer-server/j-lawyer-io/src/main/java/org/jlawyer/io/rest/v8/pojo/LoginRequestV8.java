package org.jlawyer.io.rest.v8.pojo;

/**
 * Request body of the browser login endpoint ({@code POST /v8/auth/login}). The optional
 * {@code otp} carries the second factor; it is reserved for the two-factor-auth change and
 * currently accepted but not enforced (see OpenSpec change {@code add-web-client}).
 *
 * @author jens
 */
public class LoginRequestV8 {

    private String username;
    private String password;
    private String otp;

    public LoginRequestV8() {
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getOtp() {
        return otp;
    }

    public void setOtp(String otp) {
        this.otp = otp;
    }
}
