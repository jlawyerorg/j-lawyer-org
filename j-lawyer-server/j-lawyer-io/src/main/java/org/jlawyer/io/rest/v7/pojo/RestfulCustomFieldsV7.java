package org.jlawyer.io.rest.v7.pojo;

/**
 * The labels of the three custom fields (eigene Felder) for one entity type (address, case or
 * involved party). An empty label means the field is not in use and should not be shown.
 */
public class RestfulCustomFieldsV7 {

    private String label1 = "";
    private String label2 = "";
    private String label3 = "";

    public RestfulCustomFieldsV7() {
    }

    public String getLabel1() {
        return label1;
    }

    public void setLabel1(String label1) {
        this.label1 = label1;
    }

    public String getLabel2() {
        return label2;
    }

    public void setLabel2(String label2) {
        this.label2 = label2;
    }

    public String getLabel3() {
        return label3;
    }

    public void setLabel3(String label3) {
        this.label3 = label3;
    }

}
