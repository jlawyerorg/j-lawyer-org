package org.jlawyer.io.rest.v6.pojo;

/**
 * Minimal id + display-name pair, used to offer selectable options (calendars, mailboxes, invoice
 * number pools) without exposing the underlying setup entities (which may carry secrets).
 */
public class RestfulIdNameV6 {

    private String id;
    private String name;

    public RestfulIdNameV6() {
    }

    public RestfulIdNameV6(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
