        String preview = "";
        try {
            PreviewGenerator pg = new PreviewGenerator(this.archiveFileDocumentsFacade);
            preview = pg.createPreview(aFile.getId(), db.getName());
        } catch (Throwable t) {
            log.error("Error creating document preview", t);
        }

